#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	size_t	i;
	char	*temp;

	i = 0;
	temp = dst;
	if (n > 0)
	{
		while (i < n)
		{
			temp[i] = '\0';
			i++;
		}
	}
}
