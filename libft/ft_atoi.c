#include "libft.h"

int	ft_atoi(const char *str)
{
	double			total;
	unsigned int	i;
	int				sinal;

	total = 0;
	i = 0;
	sinal = 1;
	if (ft_strlen(str) == 0)
		return (0);
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == '\r' || str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		sinal = (-1);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		total = total * 10 + str[i] - '0';
		i++;
	}
	return (sinal * total);
}
